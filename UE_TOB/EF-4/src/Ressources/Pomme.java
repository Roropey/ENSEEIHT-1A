package Ressources;

import Utile.*;

public class Pomme extends Ressource{

    /** Constructeur d'une pomme dont la position est aléatoire dans un intervalle
     * @param min_x absicisse minimale de la pomme
     * @param max_x absicisse maximale de la pomme
     * @param min_y ordonnée minimale de la pomme
     * @param max_y ordonnée maximale de la pomme
     */
    public Pomme(int min_x, int max_x, int min_y, int max_y){
        super(min_x, max_x, min_y, max_y,"Pomme",2,Categorie.fruit);
    }

    /** Constructeur d'une pomme personnalisable dont la position connue
     * @param x absicisse de la pomme (>=0)
     * @param y ordonnée de la pomme (>=0)
     * @param nom nom de la pomme
     * @param nutrition valeur nutritionnaire de la pomme
     * @param categorie catégorie de la pomme
     */
    public Pomme(int x, int y){
        super(x, y,"Pomme",2,Categorie.fruit);
    }

}